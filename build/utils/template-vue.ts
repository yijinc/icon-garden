export default (svgString: string = ''): string => {
  
  const isWithStroke: boolean = /stroke/i.test(svgString);

  const defaultSize = Number(/(?<=\bwidth=")\d+(?=\b")/ig.exec(svgString)?.[0]);

  const defaultColor = isWithStroke ?
    /(?<=\bstroke=")(\w|#)+(?=\b")/ig.exec(svgString)?.[0] : 
    /(?<=\bfill=")(\w|#)+(?=\b")/i.exec(svgString)?.[0];

  const str = svgString.replace(/^<svg\s/i, '<svg {...extra} ')
    .replace(/(?<=\bwidth=)"\d+\b"/i, '{size}')
    .replace(/(?<=\bheight=)"\d+\b"/i, '{size}')
    .replace(/(?<=\bstroke=)"(\w|#)+\b"/ig, '{color}')
    .replace(/(?<=\bfill=)"[^none](\w|#)+\b"/ig, '{color}');
  return `
/**
 * @author Auto Generated by IconGarden
 */

import React from 'react';
import { IIconProps } from '../type';

export default ({
  size = ${defaultSize},
  color = "${defaultColor}",
  ...extra
}: IIconProps) => (
  ${str}
);
`
};